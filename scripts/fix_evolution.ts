let currentId = ''
let changed = 0

for (let i = 0; i < lines.length; i++) {
  const idMatch = lines[i].match(/id: '(M\d+)'/)
  if (idMatch) currentId = idMatch[1]

  if (currentId && evoMap[currentId] && lines[i].includes('evolution:')) {
    const [prev, desc] = evoMap[currentId]
    const parts = desc.split(' → ')
    const family = parts.length >= 2 ? parts[1].replace(/^중등 /, '') : '수학의 발전'
    const prevStr = prev ? `'${prev}'` : 'undefined'

    let depth = 0, endIdx = i
    for (let j = i; j < Math.min(lines.length, i + 15); j++) {
      for (const ch of lines[j]) {
        if (ch === '{') depth++
        if (ch === '}') { depth--; if (depth === 0) { endIdx = j; break } }
      }
      if (depth === 0 && endIdx > i) break
    }

    const indent = lines[i].match(/^(\s*)/)?.[1] || '    '
    const newEvo = `${indent}evolution: { prev: ${prevStr}, next: undefined, family: '${family}', familyDescription: '${desc}' },`
    lines.splice(i, endIdx - i + 1, newEvo)
    changed++
    currentId = ''
  }
}

fs.writeFileSync('data/formulas/middle.ts', lines.join('\n'))
console.log(`Evolution updated: ${changed} / 76`)
